export function getMapImage(mapId: number) {
    const map = maps.find((m) => m.mapId === mapId);
    if (map == null) {
        return '/maps/cm_generic.png';
    }
    return `https://aoe2companion.com/maps/${map.file}.png`;
    // return `http://localhost:4200/maps/${map.file}.png`;
}

export function getMapEnumFromId(mapId: number) {
    const map = maps.find((m) => m.mapId === mapId);
    if (map == null) {
        return 'unknown';
    }
    return map.enum;
}

export function getMapIdFromEnum(mapEnum: string) {
    const map = maps.find((m) => m.enum === mapEnum);
    if (map == null) {
        return 'unknown';
    }
    return map.mapId;
}
